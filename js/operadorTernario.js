var uno = 20;
var dos = 5;

//Los operadores ternarios nos permiten hacer una evaluacion rapida y dar un resultado rapido

//condicion ? true : false (el ? seria un (Si es verdadero)) (y el : seria un (si no es verdadero))

document.write(uno==dos ? true : false);
document.write(uno==dos ? "Es igual" : "No es igual<br>");

if(uno==dos){
    document.write("es igual");
}else{
    document.write("no es igual para nada");
}